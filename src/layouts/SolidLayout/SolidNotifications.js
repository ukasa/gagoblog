export default function SolidNotifications() {
  const [anchorEl, setAnchorEl] = React.useState(null);
  const [badge, setBadge] = React.useState(10);

  const handleClick = (event) => {
    setAnchorEl(event.currentTarget);
  };

  const handleClose = () => {
    setAnchorEl(null);
  };

  const open = Boolean(anchorEl);
  const id = open ? 'simple-popover' : undefined;

  return (
    <div>
      <IconButton
        aria-describedby={id}
        aria-haspopup="true"
        onClick={handleClick}
        color="inherit"
      >
        {badge ? (
          <Badge badgeContent={badge} color="secondary">
            <NotificationsIcon />
          </Badge>
        ) : (
            <NotificationsIcon />
          )}
      </IconButton>
      <Popover
        id={id}
        open={open}
        anchorEl={anchorEl}
        onClose={handleClose}
        anchorOrigin={{
          vertical: 'bottom',
          horizontal: 'center',
        }}
        transformOrigin={{
          vertical: 'top',
          horizontal: 'center',
        }}
      >
        <SolidNotificationItems />
      </Popover>
    </div>
  );
}

